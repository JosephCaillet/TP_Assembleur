;--- Fonction de tri de tableau. Le nombre le plus hauut sera en dernière position du tableau ---;
min_max:
	ld x,#0
	ld a,(tableau,x)
	ld minimum,a
	ld maximum,a
boucle:
	inc x
	ld a,(tableau,x)
	cp a,minimum
	jruge tmax
	ld minimum, a
tmax:
	cp a,maximum
	jrule tbou
	ld maximum, a
tbou:
	cp x,#7
	jrne boucle
	ret

;************************************************************************
;
;  FIN DE LA ZONE DE DECLARATION DES SOUS-PROGRAMMES
;
;************************************************************************


;************************************************************************
;
;  PROGRAMME PRINCIPAL
;
;************************************************************************

main:
	RSP			; Reset Stack Pointer

debut:
	clr maximum
	clr minimum
	call min_max
	jp debut


;************************************************************************
;
;  ZONE DE DECLARATION DES SOUS-PROGRAMMES D'INTERRUPTION
